# 키워드 → 관련 문서 매핑 (간단한 BM25 대신 태그 기반 검색)
_TAG_INDEX: dict[str, list[str]] = {}
for doc in DOCUMENTS:
    for tag in doc["tags"]:
        _TAG_INDEX.setdefault(tag, []).append(doc["id"])

_DOC_MAP = {doc["id"]: doc for doc in DOCUMENTS}


def retrieve(query_tags: list[str], max_docs: int = 3) -> list[dict]:
    """태그 기반 관련 문서 검색 — 매칭 수 기준 랭킹."""
    scores: dict[str, int] = {}
    for tag in query_tags:
        for doc_id in _TAG_INDEX.get(tag, []):
            scores[doc_id] = scores.get(doc_id, 0) + 1

    ranked = sorted(scores.items(), key=lambda x: -x[1])
    return [_DOC_MAP[doc_id] for doc_id, _ in ranked[:max_docs]]


def get_all_as_text() -> str:
    """전체 지식베이스를 하나의 텍스트로 반환 (프롬프트 캐싱용)."""
    parts = []
    for doc in DOCUMENTS:
        parts.append(f"## {doc['title']}\n{doc['content'].strip()}")
    return "\n\n---\n\n".join(parts)
